/**
 * Get current UI language
 * @returns {string} Language code (default: 'en')
 */
export async function getUILanguage() {
  try {
    const result = await chrome.storage.local.get(['ui_language']);
    return result.ui_language || 'en';
  } catch {
    return 'en';
  }
}

/**
 * Set UI language
 * @param {string} langCode - Language code
 */
export async function setUILanguage(langCode) {
  try {
    await chrome.storage.local.set({ ui_language: langCode });
  } catch (error) {
    console.error('Failed to set UI language:', error);
  }
}

/**
 * Get localized string
 * @param {string} key - Translation key
 * @param {string} langCode - Optional language code (defaults to current UI language)
 * @returns {string} Localized string
 */
export async function t(key, langCode = null) {
  const lang = langCode || await getUILanguage();
  const locale = UI_LOCALES[lang] || UI_LOCALES.en;
  return locale[key] || key;
}

/**
 * Get localized string synchronously (requires langCode)
 * @param {string} key - Translation key
 * @param {string} langCode - Language code
 * @returns {string} Localized string
 */
export function tSync(key, langCode) {
  const locale = UI_LOCALES[langCode] || UI_LOCALES.en;
  return locale[key] || key;
}

